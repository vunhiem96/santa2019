package com.example.santa2019.Model;

public class RingFa {
    public int id;
    public String title;





    public RingFa(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public RingFa() {

    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



}


